    private void dangKyTK(String hoTen, String sdt, String email, String mk) {
        Connection ketNoi = KetNoiMSSQL1.layKetNoi();
        String sql = "INSERT INTO TK(HOTEN, SDT, EMAIL, MK) values (?, ?, ?, ?)";
        try {
            PreparedStatement ps = ketNoi.prepareStatement(sql);
            ps.setString(1, hoTen);
            ps.setString(2, sdt);
            if (email.isEmpty() || null == email) {
                ps.setNull(3, java.sql.Types.NVARCHAR);
            } else {
                ps.setString(3, email);
            }
            ps.setString(4, mk);
            ps.executeUpdate();
            JOptionPane.showMessageDialog(this, "Dang ky thanh cong!!!");
        } catch (SQLException ex) {
            System.out.println("Dang ky khong thanh cong");
        }
    }

