    private void jButton3MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jButton3MouseClicked
        String hoTen = jTFHoTen.getText().trim();
        String sdt = jTFSDT.getText().trim();
        String email = jTFEmail.getText().trim();
        String mk = new String(jPasswordField1.getPassword()).trim();

        if (hoTen.equals("Nhập họ và tên")) {
            jLCheckName.setVisible(true);
            jTFHoTen.setBorder(BorderFactory.createLineBorder(new Color(255, 0, 0)));
            jTFHoTen.requestFocus();
            return;
        }
        if (sdt.equals("Nhập số điện thoại")) {
            jLCheckPhoneNumber.setVisible(true);
            jTFSDT.setBorder(BorderFactory.createLineBorder(new Color(255, 0, 0)));
            jTFSDT.requestFocus();
            return;
        }
        if (email.equals("Nhập email")) {
            email = "";
        }
        if (mk.equals("Nhập mật khẩu")) {
            jLCheckPass.setVisible(true);
            jPasswordField1.setBorder(BorderFactory.createLineBorder(new Color(255, 0, 0)));
            jPasswordField1.requestFocus();
            return;
        }
        dangKyTK(hoTen, sdt, email, mk);
    }//GEN-LAST:event_jButton3MouseClicked

